DROP TABLE IF EXISTS users CASCADE;

CREATE TABLE users (
  id serial PRIMARY KEY NOT NULL,
  name varchar(255) NOT NULL UNIQUE,
  email varchar(255) NOT NULL,
  password_hash varchar(255) NOT NULL,
  avatar varchar(255) NOT NULL,
  latitude varchar(255) NOT NULL,
  longitude varchar(255) NOT NULL,
  created_at timestamp DEFAULT NOW(),
  deleted_at timestamp,
  notifications boolean DEFAULT FALSE
);

DROP TABLE IF EXISTS portfolios CASCADE;

CREATE TABLE portfolios (
  id serial PRIMARY KEY NOT NULL,
  user_id integer NOT NULL REFERENCES users (id) ON DELETE CASCADE,
  name varchar(255),
  value decimal NOT NULL,
  cash decimal NOT NULL,
  buying_power decimal NOT NULL,
  created_at timestamp DEFAULT NOW(),
  deleted_at timestamp,
  UNIQUE (user_id, name),
  is_public boolean DEFAULT TRUE
);

DROP TABLE IF EXISTS stocks CASCADE;

CREATE TABLE stocks (
  id serial PRIMARY KEY NOT NULL,
  symbol varchar(255) NOT NULL,
  name varchar(255) NOT NULL,
  description text,
  market varchar(255)
);
DROP TABLE IF EXISTS portfolios_stocks CASCADE;

CREATE TABLE portfolios_stocks (
  id serial PRIMARY KEY NOT NULL,
  stock_id integer NOT NULL REFERENCES stocks (id) ON DELETE CASCADE,
  portfolio_id integer NOT NULL REFERENCES portfolios (id) ON DELETE CASCADE,
  quantity integer NOT NULL,
  unique (portfolio_id, stock_id)
);
DROP TABLE IF EXISTS stock_histories CASCADE;

CREATE TABLE stock_histories (
  id serial PRIMARY KEY NOT NULL,
  stock_id integer NOT NULL UNIQUE REFERENCES stocks (id) ON DELETE CASCADE,
  TIME_SERIES_INTRADAY jsonb,
  TIME_SERIES_DAILY jsonb,
  TIME_SERIES_WEEKLY jsonb
);

DROP TABLE IF EXISTS stock_transactions CASCADE;

CREATE TABLE stock_transactions (
  id serial PRIMARY KEY NOT NULL,
  portfolio_id integer NOT NULL REFERENCES portfolios (id) ON DELETE CASCADE,
  stock_id integer NOT NULL REFERENCES stocks (id) ON DELETE CASCADE,
  quantity integer NOT NULL,
  date_time timestamp DEFAULT NOW(),
  type varchar(255) NOT NULL,
  value decimal NOT NULL
);

  DROP TABLE IF EXISTS portfolio_histories CASCADE;

  CREATE TABLE portfolio_histories (
    id serial PRIMARY KEY NOT NULL,
    portfolio_id integer NOT NULL REFERENCES portfolios (id) ON DELETE CASCADE,
    date_time timestamp DEFAULT NOW(),
    value decimal NOT NULL
  );

DROP TABLE IF EXISTS cryptos CASCADE;

CREATE TABLE cryptos (
  id serial PRIMARY KEY NOT NULL,
  symbol varchar(255) NOT NULL,
  name varchar(255) NOT NULL,
  description text,
  market varchar(255)
);

DROP TABLE IF EXISTS portfolios_cryptos CASCADE;

CREATE TABLE portfolios_cryptos (
  id serial PRIMARY KEY NOT NULL,
  crypto_id integer NOT NULL REFERENCES cryptos (id) ON DELETE CASCADE,
  portfolio_id integer NOT NULL REFERENCES portfolios (id) ON DELETE CASCADE,
  quantity decimal NOT NULL,
  unique (portfolio_id, crypto_id)
);

DROP TABLE IF EXISTS crypto_histories CASCADE;

CREATE TABLE crypto_histories (
  id serial PRIMARY KEY NOT NULL,
  crypto_id integer NOT NULL UNIQUE REFERENCES cryptos (id) ON DELETE CASCADE,
  DIGITAL_CURRENCY_DAILY jsonb,
  DIGITAL_CURRENCY_WEEKLY jsonb
);

DROP TABLE IF EXISTS crypto_transactions CASCADE;

CREATE TABLE crypto_transactions (
  id serial PRIMARY KEY NOT NULL,
  portfolio_id integer NOT NULL REFERENCES portfolios (id) ON DELETE CASCADE,
  crypto_id integer NOT NULL REFERENCES cryptos (id) ON DELETE CASCADE,
  quantity decimal NOT NULL,
  date_time timestamp DEFAULT NOW() NOT NULL,
  type varchar(255) NOT NULL,
  value decimal NOT NULL
);

